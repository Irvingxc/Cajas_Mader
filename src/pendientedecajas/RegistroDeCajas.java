    private void btn_guardar_cajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardar_cajaActionPerformed
        // TODO add your handling code here:
        
        if(txtmarca_registro.getText().isEmpty() || txtvitola_registro.getText().isEmpty() || txtnombre_registro.getText().isEmpty()
                || txtcapa_registro.getText().isEmpty() || txtempaque_registro.getText().isEmpty() || txtinventarioInicial_registro.getText().isEmpty()
         ){
           
            JOptionPane.showMessageDialog(this, "Por favor llenar todos los campos.");
        }else{
        try {
            PreparedStatement pst = cn.prepareStatement("INSERT INTO registroCajas (marca,vitola,nombre,capa,tipo_empaque,invent_inicial) VALUES (?,?,?,?,?,?)");

            //pst.setString(1, txt_id_registro.getText());
            pst.setString(1, txtmarca_registro.getText());
            pst.setString(2, txtvitola_registro.getText());
            pst.setString(3, txtnombre_registro.getText());
            pst.setString(4, txtcapa_registro.getText());
            pst.setString(5, txtempaque_registro.getText());
            pst.setString(6, txtinventarioInicial_registro.getText());

            pst.executeUpdate();
            mostrardatos("");
            limpiar();
        } catch (Exception e) {
            System.out.print(e.getMessage());
        }
        
        }
    }//GEN-LAST:event_btn_guardar_cajaActionPerformed

