    void mostrardatos(String valor) {
        DefaultTableModel modelo = new DefaultTableModel() {

            //Quitar edición de las tablas
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        modelo.addColumn("Id");
        modelo.addColumn("Marca");
        modelo.addColumn("Vitola");
        modelo.addColumn("Nombre");
        modelo.addColumn("Capa");
        modelo.addColumn("Tipo de Empaque");
        modelo.addColumn("Iventario Inicial");
        tbregistro_cajas.setModel(modelo);
        tbregistro_cajas.getTableHeader().setReorderingAllowed(false);
        String sql = "";
        if (valor.equals("")) {
            sql = "SELECT * FROM registroCajas";
        } else {
            sql = "SELECT * FROM registroCajas WHERE marca LIKE '%" + valor + "%'";
        }

        String[] datos = new String[7];
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString(1);

                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                datos[6] = rs.getString(7);
                modelo.addRow(datos);
            }
            tbregistro_cajas.setModel(modelo);
            tbregistro_cajas.getTableHeader().setReorderingAllowed(false);
            
        } catch (SQLException ex) {
            Logger.getLogger(RegistroDeCajas.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

