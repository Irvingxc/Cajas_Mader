    private void txtbuscar_registroKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbuscar_registroKeyTyped
        // TODO add your handling code here:
        //METODO PARA BUSCAR
        txtbuscar_registro.setText(txtbuscar_registro.getText().replaceAll("( )+", " "));
        //Validaciones.Space(txt_buscar_pedidos.getText().length(), evt);        
        DefaultTableModel cajas = (DefaultTableModel) tbregistro_cajas.getModel();
        txtbuscar_registro.addKeyListener(new KeyAdapter() {

            @Override
            public void keyReleased(KeyEvent ke) {
                trs.setRowFilter(RowFilter.regexFilter("(?i)" + txtbuscar_registro.getText(), 1, 2, 3, 4));
                //trs.setRowFilter(RowFilter.regexFilter("(?i)"+txtbuscar_ver_cajas.getText(), 3));

                super.keyReleased(ke); //To change body of generated methods, choose Tools | Templates.
            }

        });
        trs = new TableRowSorter(cajas);
        tbregistro_cajas.setRowSorter(trs);
    }//GEN-LAST:event_txtbuscar_registroKeyTyped

